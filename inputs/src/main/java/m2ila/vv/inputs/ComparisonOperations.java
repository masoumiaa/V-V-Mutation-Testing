package m2ila.vv.inputs;

public class ComparisonOperations {

	public boolean Lower(double a, double b){
		return a < b;
	}
	
	public boolean LowerOrEqual(double a, double b){
		return a <= b;
	}
	
	public boolean Higher(double a, double b){
		return a > b;
	}
	
	public boolean HigherOrEqual(double a, double b){
		return a >= b;
	}
}
